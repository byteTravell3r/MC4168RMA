/**
 * @brief  The application entry point.
 * @retval int
 */
int main(void) {

	/* USER CODE BEGIN 1 */

	/* USER CODE END 1 */

	/* MCU Configuration--------------------------------------------------------*/

	/* Reset of all peripherals, Initializes the Flash interface and the Systick. */
	HAL_Init();

	/* USER CODE BEGIN Init */

	/* USER CODE END Init */

	/* Configure the system clock */
	SystemClock_Config();

	/* USER CODE BEGIN SysInit */

	/* USER CODE END SysInit */

	/* Initialize all configured peripherals */
	MX_GPIO_Init();
	MX_CAN1_Init();
	MX_CAN2_Init();
	MX_UART7_Init();
	MX_SPI1_Init();
	MX_SPI5_Init();
	MX_TIM1_Init();
	MX_TIM2_Init();
	MX_TIM4_Init();
	MX_TIM5_Init();
	MX_TIM8_Init();
	MX_UART8_Init();
	MX_USART1_UART_Init();
	MX_USART3_UART_Init();
	MX_USART6_UART_Init();
	MX_CRC_Init();

	/* Initialize interrupts */
	MX_NVIC_Init();
	/* USER CODE BEGIN 2 */
	int set_spd[4];
	HAL_GPIO_WritePin(GPIOH, POWER3_CTRL_Pin, GPIO_PIN_SET);

	my_can_filter_init_recv_all(&hcan1);
	HAL_CAN_Receive_IT(&hcan1, CAN_RX_FIFO0);
	HAL_CAN_Receive_IT(&hcan2, CAN_RX_FIFO0);
	for (int i = 0; i < 4; i++) {
		PID_struct_init(&pid_spd[i], POSITION_PID, 20000, 20000, 1.5f, 0.1f,
				0.0f);  //4 motors angular rate close loop.
	}

	HAL_Delay(100);
	/* USER CODE END 2 */

	/* Infinite loop */
	/* USER CODE BEGIN WHILE */
	while (1) {
		/* USER CODE END WHILE */

		/* USER CODE BEGIN 3 */

		for (int i = 0; i < 4; i++) {
			pid_calc(&pid_spd[i], moto_chassis[i].speed_rpm, set_spd[i]);
		}
		set_moto_current(&hcan1, pid_spd[0].pos_out, pid_spd[1].pos_out,
				pid_spd[2].pos_out, pid_spd[3].pos_out);
		static int key_sta, key_cnt;
		switch (key_sta) {
		case 0:	//no key
			if (0 == HAL_GPIO_ReadPin(BUTTON_GPIO_Port, BUTTON_Pin)) {
				key_sta = 1;
			}
			break;
		case 1: //key down wait release.
			if (0 == HAL_GPIO_ReadPin(BUTTON_GPIO_Port, BUTTON_Pin)) {
				key_sta = 2;
				key_cnt++;
			} else {
				key_sta = 0;
			}
			break;
		case 2:
			if (0 != HAL_GPIO_ReadPin(BUTTON_GPIO_Port, BUTTON_Pin)) {
				key_sta = 0;
			}
			break;
		}
		if (key_cnt > 10)
			key_cnt = 0;
		set_spd[0] = set_spd[1] = set_spd[2] = set_spd[3] = key_cnt * 500;
		HAL_Delay(10);

	}
	/* USER CODE END 3 */
}

